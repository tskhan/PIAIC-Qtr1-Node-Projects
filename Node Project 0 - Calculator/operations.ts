function sum(num1: number, num2: number) {
    return num1+num2
}

export {sum};


function subtract(num1: number, num2: number) {
    return num1-num2
}

export {subtract};


function multiply(num1: number, num2: number) {
    return num1*num2
}

export {multiply};


function divide(num1: number, num2: number) {
    return num1/num2
}

export {divide};